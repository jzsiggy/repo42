int		ft_find_next_prime(int nb)
{
	int div;
	
	while (1)
	{
		div = 2;
		if (nb == 0 || nb == 1)
		{
			nb++;
		}
		else
		{
			while (div < nb / 2)
			{
				if (nb % div == 0)
				{
					nb++;
					break;
				}
			}
			return (nb);
		}
	}
}

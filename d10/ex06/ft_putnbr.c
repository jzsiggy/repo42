#include <unistd.h>

void ft_putnbr(int nb)
{
	char c;
	if (nb < 0)
	{
		c = '-';
		write(1, c, 1);
		nb = -1 * nb;
	}
	if (nb / 10 != 0)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		c = '0' + nb;
		write(1, &c, 1);
	}
}

#include <unistd.h>

int		main(void)
{
	int		n;
	char	b;
	char 	l;
	char 	*ptr;

	ptr = "aBcDeFgHiJkLmNoPqRsTuVwXyZ";
	n = 0;
	while (ptr[n] != '\0')
	{
		l = ptr[n];
		write(1, &l, 1);
		n++;
	}
	b = '\n';
	write(1, &b, 1);
}


#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb / 10 != 0)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar('0' + nb);
}

int		main(int argc, char **argv)
{
	int nb;

	nb = argc - 1;
	ft_putnbr(nb);
	ft_putchar('\n');
	return (0);
}
